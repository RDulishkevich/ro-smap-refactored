export function getRejectSuggestionChips() {
    const chips = [];
    PUBLISH_RULE_SECTIONS.forEach((section) => {
        (section.items || []).forEach((item) => {
            if (!item.rejectText) return;
            chips.push({
                id: item.id,
                label: item.rejectLabel || `${item.id} · ${item.title}`,
                text: item.rejectText,
                sectionId: section.id
            });
        });
    });
    return chips;
}

export function renderPublishRulesHtml() {
    const esc = (s) => String(s || '')
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;');

    return PUBLISH_RULE_SECTIONS.map((section) => {
        const items = (section.items || []).map((item) => `
            <article class="publish-rule-item" id="rule-${esc(item.id)}">
                <h5 class="publish-rule-item__title"><span class="publish-rule-item__code">${esc(item.id)}</span> ${esc(item.title)}</h5>
                <p class="publish-rule-item__body">${esc(item.body)}</p>
            </article>
        `).join('');
        return `
            <section class="publish-rule-section">
                <h4 class="publish-rule-section__title">${esc(section.title)}</h4>
                ${section.intro ? `<p class="publish-rule-section__intro">${esc(section.intro)}</p>` : ''}
                <div class="publish-rule-section__items">${items}</div>
            </section>
        `;
    }).join('');
}

